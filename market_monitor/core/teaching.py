def query_concept(term: str):
    """用户输入的模糊查询词 → 命中的概念（key, 文本）或 None

    支持：
    • 直接命中 CONCEPTS 的 key
    • 命中 CONCEPT_ALIASES 的别名（中英文）
    • 部分匹配 alias（大小写不敏感）
    """
    if not term:
        return None
    t = term.strip().lower()

    # 1) 直接命中 key
    if t in CONCEPTS:
        return (t, CONCEPTS[t])

    # 2) alias 精确匹配
    for alias, key in CONCEPT_ALIASES.items():
        if alias.lower() == t:
            return (key, CONCEPTS[key])

    # 3) alias 包含匹配
    for alias, key in CONCEPT_ALIASES.items():
        if t in alias.lower() or alias.lower() in t:
            return (key, CONCEPTS[key])

    return None


