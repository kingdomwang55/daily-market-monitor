def price_alert_teaching(name: str, price: float, threshold: float, direction: str) -> str:
    if direction == "down":
        pct = (price - threshold) / threshold * 100
        return (
            f"📚 教学解读\n"
            f"{CONCEPTS['stop_loss']}\n\n"
            f"当前情境：\n"
            f"• {name} 跌破 {threshold:.2f}（当前 {price:.2f}，超出 {pct:.2f}%）\n"
            f"• 该点位是你设置的止损线，触发意味着**原有买入逻辑可能已失效**\n"
            f"• 下一步不是慌，而是问自己：**当初买它的理由，现在还成立吗？**\n"
            f"  - 如果基本面没变、只是短期恐慌 → 观察不动作\n"
            f"  - 如果逻辑已破 → 按计划离场，避免侥幸"
        )
    else:  # up
        pct = (price - threshold) / threshold * 100
        return (
            f"📚 教学解读\n"
            f"{CONCEPTS['add_position']}\n\n"
            f"当前情境：\n"
            f"• {name} 突破 {threshold:.2f}（当前 {price:.2f}，超出 {pct:.2f}%）\n"
            f"• 该点位是你设置的加仓线，触发意味着**趋势可能确认**\n"
            f"• 下一步不是立刻追买，而是问自己：\n"
            f"  - 突破时**成交量**放大了吗？（放量突破更可信）\n"
            f"  - 大盘环境配合吗？（个股突破 vs 整体逆势，含义不同）\n"
            f"  - 我原本的仓位分配上限是多少？（避免情绪化超配）"
        )


